def draw_tilemap(screen, camera):
    for row_index, row in enumerate(tilemap):
        for col_index, tile in enumerate(row):
            x = col_index * TILE_SIZE - camera.offset_x
            y = row_index * TILE_SIZE - camera.offset_y

            if tile == 0:
                screen.blit(grass_img, (x, y))
            elif tile == 1:
                screen.blit(dirt_img, (x, y))
            elif tile == 2:
                screen.blit(stone_img, (x, y))


class Player:
    def __init__(self, x, y):
        self.image = player_image
        self.rect = self.image.get_rect(topleft=(x, y))
        self.velocity = 1
        self.jump_power = -4
        self.gravity = 0.15
        self.y_velocity = 0
        self.grounded = False

    def move(self, keys, dt):
        speed = self.velocity * dt * 60
        if keys[pygame.K_LEFT] or keys[pygame.K_a]:
            self.rect.x -= speed
        if keys[pygame.K_RIGHT] or keys[pygame.K_d]:
            print("Moving right")
            self.rect.x += speed
        if (keys[pygame.K_SPACE] or keys[pygame.K_w] or keys[pygame.K_UP]) and self.grounded:
            self.jump()

    def apply_gravity(self, platforms):
        self.y_velocity += self.gravity
        self.y_velocity = min(self.y_velocity, 10)
        self.rect.y += self.y_velocity
        self.grounded = False

        for platform in platforms:
            if self.rect.colliderect(platform.rect):
                if self.y_velocity > 0:  # Only snap to the platform when falling
                    self.rect.bottom = platform.rect.top
                    self.y_velocity = 0
                    self.grounded = True

    def jump(self):
        self.y_velocity = self.jump_power

    def draw(self, screen):
        screen.blit(self.image, self.rect.topleft)





class Platform:
    def __init__(self, x, y, width, height):
        self.rect = pygame.Rect(x, y, width, height)

class Camera:
    def __init__(self, width, height):
        self.offset_x = 0
        self.offset_y = 0
        self.width = width
        self.height = height

    def update(self, player):
        self.offset_x = player.rect.x - self.width // 2
        self.offset_y = player.rect.y - self.height // 2

        self.offset_x = max(0, min(self.offset_x, (len(tilemap[0]) * TILE_SIZE) - self.width))
        self.offset_y = max(0, min(self.offset_y, (len(tilemap[0]) * TILE_SIZE) - self.height))

